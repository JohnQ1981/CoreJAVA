package day03ifstatementnt;

public class OperatorSigns {

	public static void main(String[] args) {
		/*
		Comparison Operators:
		we use <,>,<=,>=, == , != to compare values 
		
		6 operators 
		Note: Comparison operators Return Boolean all the times in Java
		it is True or False
		 */
		System.out.println(5<13);// True
		System.out.println(15<13);//False
		System.out.println(5<=13); // when one is true and False= true
		// when you use more than one sign and one is true than it is true

		System.out.println(5>=5);
		//double equal sign in Java
		System.out.println(5==6);
		 //single Equal sign = is assignment a value to a variable
	
		System.out.println(5!=6);
		System.out.println(5!=5);
		
		
		
		
	}

}
