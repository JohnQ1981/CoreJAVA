public class ForLoop02 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
int i;
		for(i=1; i<=10;i++) {
			System.out.printf("%d*%d=%d",5,i,5*i).println();
			System.out.println("\n");
			System.out.printf("%d*%d=%d",6,i,6*i).println();
			System.out.println("\n");
			System.out.println(i);
			System.out.println("\n");
			
		}		
		
		
		
		
		
		

	}

}
